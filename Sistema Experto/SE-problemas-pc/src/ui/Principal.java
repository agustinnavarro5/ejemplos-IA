    private void setFact(String hecho, ItemEvent evt) {
        Object item = evt.getItem();

        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (!"none".equals(item.toString().toLowerCase()))
                this._rie.addFact(hecho, item.toString().toLowerCase());
        }
    }

