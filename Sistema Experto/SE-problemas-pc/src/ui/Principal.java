    private void btn_diagnosticarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_diagnosticarActionPerformed
        _rie.infer();
        
        boolean problema_memoria_ram = false;
        boolean problema_disco_duro = false;
        boolean problema_paca_mother = false;

        if (_rie.getFactsAboutVariable(Hechos.PROBLEMA_MEMORIA_RAM).size() > 0) {
            problema_memoria_ram = true;
        }
        if (_rie.getFactsAboutVariable(Hechos.PROBLEMA_DISCO_DURO).size() > 0) {
            problema_disco_duro = true;
        }
        if (_rie.getFactsAboutVariable(Hechos.PROBLEMA_PLACA_MOTHER).size() > 0) {
            problema_paca_mother = true;
        }
        
        String diagnostico;
        if (problema_memoria_ram || problema_disco_duro || problema_paca_mother) {
            diagnostico = "Posibles problemas: ";
            if (problema_memoria_ram)
                diagnostico += " Memoria Ram";
           
            if (problema_disco_duro)
                diagnostico += ", Disco Duro";
            
            if (problema_paca_mother)
                diagnostico += ", Placa Mother";
        } else {
            diagnostico = "No se pudo determinar el Error.";
        }
        
        resultado_diagnostico.setText(diagnostico);
        
        _rie.clearFacts();
        
        pitido_largo.setSelectedIndex(0);
        pantalla_enciende.setSelectedIndex(0);
        so_inicia.setSelectedIndex(0);
        pc_enciende.setSelectedIndex(0);
        monitor_imagen.setSelectedIndex(0);
        pc_reinicios.setSelectedIndex(0);
        pitido_continuos.setSelectedIndex(0);
        tres_pitidos_cortos.setSelectedIndex(0);
        so_msj_error_gral.setSelectedIndex(0);
        so_cuelga.setSelectedIndex(0);
        pc_lenta.setSelectedIndex(0);
        pantalla_azul.setSelectedIndex(0);
        error_grabar_archivo.setSelectedIndex(0);
        ruido_metalico.setSelectedIndex(0);
        scandisk_activado.setSelectedIndex(0);
    }//GEN-LAST:event_btn_diagnosticarActionPerformed

